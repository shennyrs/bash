#creating a list
mylist=[]
print("pinting blank list")
print(mylist)

#creating list contains string
mylist=['hello world']
print(" ")
print(mylist)

#creating list contains multiple values
mylist=["hello","world"]
print(" ")
print(mylist)
print(mylist[0])
print(mylist[1])

#creating multidimensional list
mylist=[["hello1","world1"],["hello2","world2"]]
print(" ")
print(mylist)
for i in mylist:
    print(i)
    
#creating list with numbers
mylist=[1,2,2,3,44,5,6]
print(mylist)
for i in mylist:
    print(i)
    
#creating a list with mixed values
mylist=[1,2,"hello",3,4,"world"]
print(mylist)
for i in mylist:
    print(i)